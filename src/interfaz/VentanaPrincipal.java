    public void actualizarTabla() {
        // 1. Conseguimos el modelo actual
        
        DefaultTableModel modelo = (DefaultTableModel) tablaCola.getModel();
    
        // 2. Limpiamos TODO lo que haya
        modelo.setRowCount(0); 

        // 3. Obtenemos el montículo
        var monticulo = sistema.getColaPrioridad();
    
        // 4. Llenamos con un ciclo
        for (int i = 1; i <= monticulo.getTamaño(); i++) {
            var registro = monticulo.getElementos()[i]; 
            if (registro != null) {
                modelo.addRow(new Object[]{
                    registro.getEtiquetaTiempo(),
                    registro.getDocumento().getNombre(),
                    registro.getDocumento().getTipo(),
                    registro.getDocumento().getNombreUsuario(), // Revisa que este método exista
                    "Prioridad " + registro.getValorPrioridad()
                });
            }
        }
    
        // 5. Esto obliga a la interfaz a refrescarse
        modelo.fireTableDataChanged();
        tablaCola.repaint();
}

