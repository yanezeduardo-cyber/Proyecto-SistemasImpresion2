    private void CancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelarActionPerformed
        // TODO add your handling code here:
        try {
            // Ahora leemos el NOMBRE DEL USUARIO del cuadro de texto, ya no un ID numérico
            String nombreUsuario = txtEtiqueta.getText().trim();
        
            if (nombreUsuario.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingresa el Nombre del Usuario para cancelar su documento.");
                return;
            }

            // Llamamos a la lógica pasándole el String directamente ¡Adiós error rojo!
            sistema.eliminarDocumentoDeCola(nombreUsuario);
        
            // Refrescamos la tabla para que el cambio se vea en la pantalla gris
            actualizarTabla(); 
        
            // Limpiamos el campo y avisamos al usuario
            txtEtiqueta.setText("");
            javax.swing.JOptionPane.showMessageDialog(this, "Si " + nombreUsuario + " tenía un documento, ha sido cancelado exitosamente.");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al cancelar: " + e.getMessage());
        }
    }//GEN-LAST:event_CancelarActionPerformed

    private void ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirActionPerformed
        // TODO add your handling code here:
        modelo.Documento docImpreso = sistema.procesarImpresion();
    
        if (docImpreso != null) {
            JOptionPane.showMessageDialog(this, "Imprimiendo: " + docImpreso.getNombre());
            actualizarTabla(); // Para que desaparezca de la lista visual
        } else {
            JOptionPane.showMessageDialog(this, "No hay nada en la cola.");
        }
    }//GEN-LAST:event_ImprimirActionPerformed

    private void CargarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarUsuariosActionPerformed
        // TODO add your handling code here:
        JFileChooser selector = new JFileChooser();
        int resultado = selector.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            String ruta = selector.getSelectedFile().getAbsolutePath();
    
            // Carga los usuarios a la Tabla Hash
            manejador.cargarUsuarios(ruta, sistema.getTablaUsuarios()); 
    
            // ACTUALIZA LA TABLA: Esto asegura que si hay documentos, se pinten de una vez
            actualizarTabla(); 
    
            JOptionPane.showMessageDialog(this, "¡Usuarios cargados con éxito!");
        }   
    }//GEN-LAST:event_CargarUsuariosActionPerformed

    private void CargarImpresionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarImpresionesActionPerformed
        // TODO add your handling code here:
        JFileChooser selector = new JFileChooser();
        int resultado = selector.showOpenDialog(this);
    
        if (resultado == JFileChooser.APPROVE_OPTION) {
            String ruta = selector.getSelectedFile().getAbsolutePath();
        
            // Cargamos los documentos al montículo
            manejador.procesarArchivoImpresion(ruta, sistema); 
        
            // ESTO ES NUEVO: Para que los datos aparezcan en la pantalla gris
            actualizarTabla(); 
        
            JOptionPane.showMessageDialog(this, "Cola de impresión actualizada.");
        }
    }//GEN-LAST:event_CargarImpresionesActionPerformed

    private void NuevoUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NuevoUsuarioActionPerformed
        // TODO add your handling code here:
        // 1. Capturamos los datos de la interfaz
        String nombre = txtNombreUsuario.getText().trim();
        String prioridad = txtPrioridad.getText().trim().toLowerCase();

        // 2. Validaciones básicas
        if (nombre.isEmpty() || prioridad.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }

        // 3. Verificamos que sea una prioridad válida según tu ManejadorCSV
        if (!prioridad.equals("prioridad_alta") && 
            !prioridad.equals("prioridad_media") && 
            !prioridad.equals("prioridad_baja")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Prioridad no válida. Usa: prioridad_alta, prioridad_media o prioridad_baja.");
            return;
        }

        // 4. Llamamos a tu lógica para registrar en la Tabla Hash
        try {
            sistema.registrarUsuario(nombre, prioridad); // Usa el método de tu clase SistemaImpresion
        
            // 5. Limpiamos los campos y avisamos
            txtNombreUsuario.setText("");
            txtPrioridad.setText("");
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario '" + nombre + "' registrado con éxito.");
        
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al registrar: " + e.getMessage());
        }
    }//GEN-LAST:event_NuevoUsuarioActionPerformed

